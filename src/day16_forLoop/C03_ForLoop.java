package day16_forLoop;

public class C03_ForLoop {

	public static void main(String[] args) {
		//10 ile 30 (10 ve 30 dahil) arasindaki sayilari aralarinda 
		//virgul olarak ayni satirda yazdirin
		for (int i1 = 10; i1 <= 30; i1++) {
			System.out.print(i1 + ",");
			System.out.println();// sadece alt satira gecmek icin kullanilir

	}

}
}